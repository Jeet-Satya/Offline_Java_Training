public class Assign {
    public static void main(String[] args) {
        int x=5;
        int y=6;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x*2);
        System.out.println(x*y);
        System.out.println(x++);
        System.out.println(x--);
        System.out.println(--y);    
        System.out.println(x%y);
        System.out.println(x^y);
    }
}
